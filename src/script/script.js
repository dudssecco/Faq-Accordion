const op1 = document.getElementById('op-1')
const clo1 = document.getElementById('clo-1')
const an1 = document.getElementById('an-1')

function Open1(){
    op1.style.display = 'none'
    clo1.style.display = 'block'
    an1.style.display = 'block'
}

function Close1(){
    op1.style.display = 'block'
    clo1.style.display = 'none'
    an1.style.display = 'none'
}

